export const SKINTONES = {
  'yellow':       { hex: '',          dec: '' },
  'light':        { hex: "&#x1F3FB;", dec: "&#127995" },
  'medium-light': { hex: "&#x1F3FC;", dec: "&#127996" },
  'medium':       { hex: "&#x1F3FD;", dec: "&#127997" },
  'medium-dark':  { hex: "&#x1F3FE;", dec: "&#127998" },
  'dark':         { hex: "&#x1F3FF;", dec: "&#127995" }
};

// Helper function to easily print emojis with the right skintone
export const printEmoji = ({emoji, skintone, hexOrDec}) => {
  // standardize whether the hex or decimal representation will be used,
  // defaulting to decimal
  hexOrDec = hexOrDec === 'dec' ? 'dec' : 'hex';

  // Defalut value of skintone is yellow
  skintone = skintone || 'yellow';

  // The emoji code is output first
  let output = [ emoji[ hexOrDec ] ];

  // If the emoji is toned, then append the skintone code afterwards
  if( emoji.toned ) output.push( SKINTONES[skintone][hexOrDec] )

  // Return the combined string of the two codes
  return output.join('');
}

